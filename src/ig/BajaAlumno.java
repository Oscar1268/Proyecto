    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        this.modTabla = (DefaultTableModel)jTable1.getModel();
        modTabla.removeRow(jTable1.getSelectedRow());
        try{
           String listaAlumnos="Alumnos.txt";
           BufferedWriter bw = new BufferedWriter(new FileWriter(listaAlumnos));
            for (int i=0; i<jTable1.getRowCount();i++) {
             for(int j=0; j<jTable1.getColumnCount();j++){
                 bw.write((String)(jTable1.getValueAt(i,j)));
                 if(j<jTable1.getColumnCount()-1){
                     bw.write(" / ");
                 }
             }
            }
            bw.close();
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,e);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

